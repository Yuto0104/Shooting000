#include "main.h"

//=============================================================================
// テクスチャクラス
// Author : 唐﨑結斗
// 概要 : テクスチャ設定を行うクラス
//=============================================================================
class CTexture
{
public:
	//*****************************************************************************
	// 定数定義
	//*****************************************************************************
	static const unsigned int MAX_NAME = (0x100);

	//--------------------------------------------------------------------
	// テクスチャの種別の列挙型
	// Author : 唐﨑結斗
	// 概要 : オブジェクトの種別情報を格納
	//--------------------------------------------------------------------
	enum TEXTURE_TYPE
	{
		TYPE_2DPOLYGON = 0,		// 2Dポリゴン
		TYPE_2DPLAYER,			// 2Dプレイヤー
		TYPE_2DBULLET,			// 2D弾
		TYPE_2DEXPLOSION,		// 2D爆発
		TYPE_2DENEMY_000,		// 2Dエネミー(0)
		TYPE_SKY_000,			// 空(0)
		TYPE_NUMBER_000,		// ナンバー(0)
		TYPE_LIFE_000,			// ハート(0)
		TYPE_FLOOR_000,			// 床(0)
		MAX_TYPE,				// 種別の最大数
		TYPE_NULL,				// テクスチャ設定なし
	};

	//--------------------------------------------------------------------
	// コンストラクタとデストラクタ
	//--------------------------------------------------------------------
	CTexture();
	~CTexture();

	//--------------------------------------------------------------------
	// メンバ関数
	//--------------------------------------------------------------------
	// 初期化
	void Init(void);

	// 終了
	void Uninit(void);		

	// テクスチャのゲッター
	LPDIRECT3DTEXTURE9 GetTexture(TEXTURE_TYPE type);

private:
	//--------------------------------------------------------------------
	// メンバ変数
	//--------------------------------------------------------------------
	// テクスチャ
	LPDIRECT3DTEXTURE9 m_pTexture[MAX_TYPE];
};

#endif

